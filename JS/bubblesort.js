function bubbleSort(arr) {
    let n = arr.length;
    let swapped;

    // Зовнішній цикл відповідає за кількість проходів
    for (let i = 0; i < n - 1; i++) {
        swapped = false; // Скидаємо прапорець на початку кожного проходу

        // Внутрішній цикл порівнює сусідні елементи
        // n - 1 - i дозволяє не перевіряти вже відсортовані елементи в кінці
        for (let j = 0; j < n - 1 - i; j++) {
            if (arr[j] > arr[j + 1]) {
                // Міняємо елементи місцями за допомогою деструктуризації
                [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]];
                swapped = true; // Фіксуємо, що заміна відбулася
            }
        }

        // Оптимізація: якщо за весь прохід не було жодної заміни, масив відсортовано
        if (!swapped) break;
    }
    return arr;
}

// Перевірка
const array1 = [64, 34, 25, 12, 22, 11, 90];
console.log("Bubble Sort:", bubbleSort(array1)); 
// Вивід: [11, 12, 22, 25, 34, 64, 90]